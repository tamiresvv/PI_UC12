            <label>Aberrometria</label><br><br>
            Os aberrômetros são equipamentos capazes de avaliar as aberrações ópticas dos olhos humanos. Aberrações ópticas, por sua vez, são imperfeições<br>
            dos sistemas ópticos que existem nos olhos humanos. Mais especificamente, estas aberrações se encontram no cristalino e na córnea de cada olho.<br><br><br>
            <label>Angiografia com fluoresceína</label><br><br>
            A angiografia fluoresceínica consiste na realização de fotos contrastadas do fundo do olho. Para tal, injeta-se na veia do paciente um contraste<br>
            chamado fluoresceína e este contraste percorre a circulação até chegar no olho.<br><br><br>
            <label>Ecografia ocular</label><br><br>
            A ecografia funciona de maneira semelhante ao radar de um submarino (sonar), através de um aparelho que emite sons de alta freqüência<br>
            (não são percebidos pelo ouvido humano). Estas ondas sonoras atravessam os tecidos, são refletidas parcialmente e captadas pelo mesmo aparelho.<br><br><br>
            <label>Papilografia</label><br><br>
            Este exame é praticamente igual à retinografia. A diferença é que a foto é focada apenas na papila (nervo óptico) e geralmente feita<br>
            em maior aumento. É utilizada principalmente para acompanhar a evolução do glaucoma onde ocorre aumento progressivo da escavação do nervo óptico.<br><br><br>
            <label>Paquimetria corneana</label><br><br>
            Este exame serve para medir a espessura da córnea. As medidas da espessura corneana são importantes no planejamento de diversas cirurgias<br>
            oculares, como as cirurgias refrativas, por exemplo. Além disso, a espessura corneana também é importante para a avaliação de algumas doenças <br>
            oculares como o ceratocone e o glaucoma.<br><br><br>
            <label>Retinografia</label><br><br>
            A retinografia é uma foto do fundo do olho, da retina. Através de uma câmera praticamente igual à da angiografia realiza-se uma foto colorida<br>
            da retina, no caso da retinografia colorida ou em preto e branco, no caso da retiografia red free.<br><br><br>
