            O teste do reflexo vermelho ou teste do olhinho é um exame simples, rápido e indolor. Ele geralmente é feito com um aparelho chamado oftalmoscópio,<br>
            através do qual é lançada uma luz nos olhos do bebê. Se tudo estivar bem, a luz emitida por esse aparelho irá atravessar as diversas camadas do olho e provocar um reflexo vermelho homogêneo<br>
            no espaço pupilar. Assim, o médico deverá avaliar se o reflexo observado é normal ou se apresenta alguma alteração.<br>
            Este teste pode detectar qualquer alteração que cause obstrução no eixo visual, como a catarata congênita, o retinoblastoma e o glaucoma congênito.<br>
            Quanto antes forem diagnosticadas estas doenças, maiores serão as chances de alcançar um tratamento adequado. Portanto, este exame deve ser feito nas primeiras<br>
            semanas de vida e pode ser realizado pelo médico pediatra ou pelo médico oftalmologista.<br><br><br>
