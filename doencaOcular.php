            <label> Aberrações de alta ordem</label><br><br>
            Aberrações ópticas de alta ordem são imperfeições dos sistemas ópticos que existem nos olhos humanos. Mais especificamente, estas<br>
            aberrações se encontram no cristalino e na córnea de cada olho.<br><br><br>
            <label>Astigmatismo</label><br><br>
            No astigmatismo, a imagem não possui um foco único, ficando espalhada ao longo de um determinado percurso. Assim, o foco da imagem<br>
            se espalha, podendo estar parcialmente (ou totalmente) antes ou após a retina.<br><br><br> 
            <label>Hipermetropia</label><br><br>
            Na hipermetropia, a imagem se forma depois da retina (ou atrás da retina, em outras palavras). Pacientes hipermétropes costumam apresentar dificuldade<br>
            na visão para perto (visão borrada), embora possam ter boa visão para longe.<br><br><br> 
            <label>Miopia</label><br><br>
            Na miopia, a imagem se forma antes da retina (ou na frente da retina, em outras palavras). Em razão disto, pacientes míopes apresentam dificuldade na visão para longe<br>
            visão borrada), embora possam ter boa visão para perto.<br><br><br>
            <label>Pseudoestrabismo</label><br><br>
            Estrabismo é o termo médico usado para desvio ocular. O termo peseudoestrabismo significa uma falsa aparência de estrabismo.<br>
            Algumas crianças apresentam uma prega de pele um pouco mais larga no canto interno das pálpebras associada a uma base nasal mais alargada (EPICANTO). Essas características contribuem para que
            a criança tenha um aspecto de ser estrábica pois o espaço entre a íris e o canto interno da pálpebra fica reduzido (tem menos área branca aparecendo).<br>
            Isso fica ainda mais evidente em fotografias.<br><br><br>
            <label>Estrabismo</label><br><br>
            Estrabismo é o desalinhamento dos olhos. Os tipos mais freqüentes de estrabismo são a esotropia (quando os olhos estão desviados “para dentro”) e a exotropia <br>
            quando os olhos estão desviados “para fora”). Estima-se que 4% da população seja portadora de algum tipo de estrabismo.<br><br><br>
            <label>Conjutivite</label><br><br>
            A conjuntivite consiste na inflamação da conjuntiva, sendo muito freqüente na população, de um modo geral. A conjuntiva, por sua vez, é uma camada delgada que reveste a porção anterior da esclera<br>
            a parte branca dos olhos) e a superfície interna das pálpebras (as peles que recobrem os olhos).<br><br><br>
            <label>Catarata</label><br><br>
            A catarata é uma doença progressiva que causa a opacificação do cristalino. O cristalino é a lente natural do olho e deve ser transparente para que os raios de luz o atravessem e alcancem a retina <br>
            onde se forma a imagem). Assim, a presença da catarata prejudica a visão, pois dificulta a entrada de luz no olho.<br><br><br><br<br>
